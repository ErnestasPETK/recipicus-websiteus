"""
Calculator functions
"""


def add(x: int, y: int) -> int:
    """
    Add two numbers together

    Args:
        x (int): first number
        y (int): second number

    Returns:
        int: sum of the two variables
    """
    return x + y


def subtract(x: int, y: int) -> int:
    """
    Subtract two numbers together

    Args:
        x (int): first number
        y (int): second number

    Returns:
        int: difference of the two variables
    """
    return x - y
